namespace CSharp
{
    // Given two numbers, return true if the sum of both numbers is less than 100. 
    // Otherwise return false.
    // https://edabit.com/challenge/3ZwEJFANGaSpqnzrs
    public static class LessThan100
    {
        public static bool SumIsLessThan100(int a, int b) => a + b < 100;
    }
}